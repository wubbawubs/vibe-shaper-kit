// Helper to get page by slug
export function getSEOPageBySlug(slug: string): SEOPage | undefined {
  return seoPages.find(page => page.slug === slug);
}

// Helper to get pages by priority
export function getSEOPagesByPriority(priority: Priority): SEOPage[] {
  return seoPages.filter(page => page.priority === priority);
}

// Helper to get pages by type
export function getSEOPagesByType(pageType: PageType): SEOPage[] {
  return seoPages.filter(page => page.pageType === pageType);
}

// Helper to get pages by language
export function getSEOPagesByLanguage(language: Language): SEOPage[] {
  return seoPages.filter(page => page.language === language);
}

// Helper to get pages by template
export function getSEOPagesByTemplate(template: Template): SEOPage[] {
  return seoPages.filter(page => page.template === template);
}

// Get all slugs (for routing)
export function getAllSEOSlugs(): string[] {
  return seoPages.map(page => page.slug);
}

// Get all indexable pages (for sitemaps)
export function getIndexableSEOPages(): SEOPage[] {
  return seoPages.filter(page => !page.noindex);
}

// Get pages for sitemap by language and type
export function getSitemapPages(language: Language, pageType?: PageType): SEOPage[] {
  return seoPages.filter(page => 
    page.language === language && 
    !page.noindex &&
    (pageType ? page.pageType === pageType : true)
  );
}

// Count pages by various criteria (for reporting)
export function getSEOPageStats() {
  return {
    total: seoPages.length,
    byPriority: {
      P0: getSEOPagesByPriority("P0").length,
      P1: getSEOPagesByPriority("P1").length,
      P2: getSEOPagesByPriority("P2").length,
    },
    byLanguage: {
      nl: getSEOPagesByLanguage("nl").length,
      en: getSEOPagesByLanguage("en").length,
      de: getSEOPagesByLanguage("de").length,
    },
    byTemplate: {
      "T-A": getSEOPagesByTemplate("T-A").length,
      "T-COMP": getSEOPagesByTemplate("T-COMP").length,
      "T-ALT": getSEOPagesByTemplate("T-ALT").length,
      "T-GUIDE": getSEOPagesByTemplate("T-GUIDE").length,
      "T-FEAT": getSEOPagesByTemplate("T-FEAT").length,
      "T-INT": getSEOPagesByTemplate("T-INT").length,
      "T-IND": getSEOPagesByTemplate("T-IND").length,
      "T-ROLE": getSEOPagesByTemplate("T-ROLE").length,
      "T-GLOSS": getSEOPagesByTemplate("T-GLOSS").length,
      "T-USE": getSEOPagesByTemplate("T-USE").length,
    },
    indexable: getIndexableSEOPages().length,
    noindex: seoPages.filter(p => p.noindex).length,
  };
}
